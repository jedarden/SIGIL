    #[test]
    fn test_recovery_code_checksum_verification() {
        let mut code = RecoveryCode::generate(0);
        assert!(code.verify_checksum());

        // Corrupt the checksum
        code.checksum[0] ^= 0xFF;
        assert!(!code.verify_checksum());
    }

    #[test]
    fn test_recovery_code_usage() {
        let usage = RecoveryCodeUsage::new(3, "device-enrollment");
        assert_eq!(usage.code_index, 3);
        assert_eq!(usage.purpose, "device-enrollment");
        assert!(usage.context.is_none());

        let usage_with_context = usage.with_context("New device enrollment".to_string());
        assert_eq!(
            usage_with_context.context.as_deref(),
            Some("New device enrollment")
        );
    }
