//! Recovery codes for sealed vault emergency access
//!
//! Recovery codes are single-use codes that can substitute for ALL other
//! authentication factors (passphrase, device key, TOTP) for emergency access.
//!
//! # Features
//!
//! - 8 single-use recovery codes generated at vault initialization
//! - SLIP39-style mnemonic encoding (27-word phrases for 32-byte codes)
//! - Each code can be used only once
//! - Usage is tracked in the vault header (visible without unsealing)
//! - Recovery codes are the ONLY way to recover if device key is lost

#![warn(missing_docs)]
#![warn(clippy::all)]

use rand::RngCore;
use serde::{Deserialize, Serialize};
use sha2::{Digest, Sha256};
use sigil_core::SigilError;

/// Number of recovery codes generated per vault
pub const RECOVERY_CODE_COUNT: usize = 8;

/// Length of recovery code checksum (in bytes)
const CHECKSUM_LENGTH: usize = 4;

/// A single recovery code
///
/// Recovery codes are 256-bit random values with a checksum,
/// encoded as SLIP39-compatible mnemonics.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RecoveryCode {
    /// Code index (0-7)
    pub index: usize,
    /// Random code value (32 bytes = 256 bits)
    #[serde(with = "serde_bytes")]
    pub value: Vec<u8>,
    /// Checksum of the code value
    #[serde(with = "serde_bytes")]
    pub checksum: Vec<u8>,
}

impl RecoveryCode {
    /// Generate a new recovery code
    ///
    /// Creates a cryptographically random 256-bit code with a SHA-256 checksum.
    pub fn generate(index: usize) -> Self {
        let mut value = vec![0u8; 32];
        let mut rng = rand::thread_rng();
        rng.fill_bytes(&mut value);

        // Calculate checksum (first 4 bytes of SHA-256)
        let checksum = Sha256::digest(&value)[0..CHECKSUM_LENGTH].to_vec();

        Self {
            index,
            value,
            checksum,
        }
    }

    /// Verify the checksum
    ///
    /// Returns true if the checksum is valid for the code value.
    pub fn verify_checksum(&self) -> bool {
        let expected = Sha256::digest(&self.value)[0..CHECKSUM_LENGTH].to_vec();
        self.checksum == expected
    }

    /// Encode as a SLIP39-style mnemonic phrase
    ///
    /// Returns a phrase using the SLIP39 wordlist (2048 words).
    /// Encodes both the 32-byte value and 4-byte checksum using bitstream encoding.
    /// This produces 27 words for a 36-byte (288-bit) code.
    pub fn to_mnemonic(&self) -> Result<String, SigilError> {
        // Bits per word for 2048-word wordlist
        const BITS_PER_WORD: usize = 11;

        // Combine value and checksum into a single buffer
        let mut combined: Vec<u8> = Vec::with_capacity(36);
        combined.extend_from_slice(&self.value);
        combined.extend_from_slice(&self.checksum);

        // Convert bytes to bitstream
        let mut bitstream = Vec::new();
        for &byte in &combined {
            for i in 0..8 {
                bitstream.push((byte >> (7 - i)) & 1 == 1);
            }
        }

        // Convert bitstream to word indices (11 bits per word)
        let word_count = bitstream.len().div_ceil(BITS_PER_WORD);
        let mut word_indices = Vec::with_capacity(word_count);

        for i in 0..word_count {
            let mut index = 0usize;
            for j in 0..BITS_PER_WORD {
                let bit_pos = i * BITS_PER_WORD + j;
                if bit_pos < bitstream.len() && bitstream[bit_pos] {
                    index |= 1 << (BITS_PER_WORD - 1 - j);
                }
            }
            word_indices.push(index);
        }

        // Convert indices to words
        let word_strs: Vec<String> = word_indices
            .iter()
            .map(|&idx| {
                slip39::get_word_at_index(idx)
                    .map(|w| w.to_string())
                    .ok_or_else(|| SigilError::Crypto(format!("Invalid word index: {}", idx)))
            })
            .collect::<Result<Vec<String>, SigilError>>()?;

        Ok(word_strs.join(" "))
    }

    /// Decode from a mnemonic phrase
    ///
    /// Parses a SLIP39-style mnemonic and reconstructs the recovery code.
    /// Expects 27 words for a valid 36-byte recovery code.
    pub fn from_mnemonic(mnemonic: &str) -> Result<Self, SigilError> {
        // Bits per word for 2048-word wordlist
        const BITS_PER_WORD: usize = 11;
        const EXPECTED_BYTES: usize = 36; // 32 bytes value + 4 bytes checksum

        let words: Vec<&str> = mnemonic.split_whitespace().collect();

        if words.is_empty() {
            return Err(SigilError::Crypto(
                "Invalid recovery code: empty mnemonic".to_string(),
            ));
        }

        // Convert words to bitstream
        let mut bitstream = Vec::new();
        for word in &words {
            let word_index = slip39::find_word_index(word)
                .ok_or_else(|| SigilError::Crypto(format!("Invalid SLIP39 word: {}", word)))?;

            // Convert index to bits
            for j in 0..BITS_PER_WORD {
                let bit = (word_index >> (BITS_PER_WORD - 1 - j)) & 1 == 1;
                bitstream.push(bit);
            }
        }

        // Convert bitstream to bytes
        let expected_bits = EXPECTED_BYTES * 8;
        if bitstream.len() < expected_bits {
            return Err(SigilError::Crypto(format!(
                "Invalid recovery code: too short (expected {} bits, got {})",
                expected_bits,
                bitstream.len()
            )));
        }

        let mut bytes = Vec::with_capacity(EXPECTED_BYTES);
        for i in 0..EXPECTED_BYTES {
            let mut byte = 0u8;
            for j in 0..8 {
                let bit_pos = i * 8 + j;
                if bit_pos < bitstream.len() && bitstream[bit_pos] {
                    byte |= 1 << (7 - j);
                }
            }
            bytes.push(byte);
        }

        if bytes.len() != EXPECTED_BYTES {
            return Err(SigilError::Crypto(
                "Failed to decode recovery code: incorrect length".to_string(),
            ));
        }

        let value = bytes[0..32].to_vec();
        let checksum = bytes[32..36].to_vec();

        let code = RecoveryCode {
            index: 0, // Will be set by caller
            value,
            checksum,
        };

        if !code.verify_checksum() {
            return Err(SigilError::Crypto(
                "Invalid recovery code: checksum verification failed".to_string(),
            ));
        }

        Ok(code)
    }

    /// Get the code value as bytes
    pub fn as_bytes(&self) -> &[u8] {
        &self.value
    }
}

/// Usage record for a recovery code
///
/// Tracks when and how a recovery code was used.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RecoveryCodeUsage {
    /// Index of the recovery code that was used (0-7)
    pub code_index: usize,
    /// Timestamp when the code was used
    pub used_at: String, // RFC3339
    /// Purpose of use (e.g., "device-enrollment", "lost-device-recovery")
    pub purpose: String,
    /// Optional additional context
    pub context: Option<String>,
}

impl RecoveryCodeUsage {
    /// Create a new recovery code usage record
    pub fn new(code_index: usize, purpose: &str) -> Self {
        Self {
            code_index,
            used_at: chrono::Utc::now().to_rfc3339(),
            purpose: purpose.to_string(),
            context: None,
        }
    }

    /// Add context to the usage record
    pub fn with_context(mut self, context: String) -> Self {
        self.context = Some(context);
        self
    }
}

/// Information about a recovery code
///
/// Returned by `list_recovery_codes` to show the status of each code.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RecoveryCodeInfo {
    /// Code index (0-7)
    pub index: usize,
    /// Whether this code has been used
    pub is_used: bool,
    /// Usage information (if used)
    pub usage_info: Option<String>,
}

